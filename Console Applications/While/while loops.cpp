// While Loop

#include <iostream>
using namespace std;

int main()
    {
        int x = 1; // assigns a value to the variable

        while(x <= 10)
            {
                cout << x << endl; // prints to the screen and ends the line
                x = x + 1; // increments the value of the variable
            }
        
        system("pause"); // pauses the command line
        return 0;
    }
